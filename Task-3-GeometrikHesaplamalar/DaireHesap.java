public class DaireHesap {

	public static void main(String[] args) {
		double r =5.5 ;
		double pi =3.1415 ;
		// Alan hesapla
		double alan = pi*r*r ;
		// Cevre hesapla 
		double cevre = 2*pi*r ;
		// Sonucları yazdır
		System.out.println("Yarıcap:" + r);
		System.out.println("Alan:" +alan);
		System.out.println("Cevre:"+cevre);

	}

}
